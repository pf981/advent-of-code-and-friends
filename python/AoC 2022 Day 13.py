import copy

def is_right_order(a, b):
  a = copy.deepcopy(a)
  b = copy.deepcopy(b)
  
  if isinstance(a, int) and isinstance(b, int):
    if a < b:
      return True
    if a > b:
      return False
    return None
  
  if isinstance(a, list) and isinstance(b, list):
    if not a and not b:
      return None
    if not a:
      return True
    if not b:
      return False
    
    result = is_right_order(a.pop(0), b.pop(0))
    if result is None:
      return is_right_order(a, b)
    return result
  
  if isinstance(a, int):
    return is_right_order([a], b)
  return is_right_order(a, [b])


pairs = [[eval(s) for s in pair.splitlines()] for pair in inp.split('\n\n')]

answer = sum(i for i, (a, b) in enumerate(pairs, 1) if is_right_order(a, b))
