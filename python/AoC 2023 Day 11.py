def get_distances_sum(empty_size):
    distance_sum = 0
    for g1, g2 in pairs:
        d_row = abs(g2[0] - g1[0])
        for row in empty_rows:
            if min(g1[0], g2[0]) < row < max(g1[0], g2[0]):
                d_row += empty_size - 1

        d_col = abs(g2[1] - g1[1])
        for col in empty_cols:
            if min(g1[1], g2[1]) < col < max(g1[1], g2[1]):
                d_col += empty_size - 1

        distance_sum += d_row + d_col

    return distance_sum


grid = inp.splitlines()
