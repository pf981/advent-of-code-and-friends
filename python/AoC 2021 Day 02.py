# MAGIC <ul>
# MAGIC <li><code>forward 5</code> adds <code>5</code> to your horizontal position, a total of <code>5</code>. Because your aim is <code>0</code>, your depth does not change.</li>
# MAGIC <li><code>down 5</code> adds <code>5</code> to your aim, resulting in a value of <code>5</code>.</li>
