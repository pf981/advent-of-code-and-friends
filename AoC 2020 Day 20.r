    tibble(
      tile_id = tiles$tile_id,
      north = str_rev(tiles$north),
      east = str_rev(tiles$west),
      south = str_rev(tiles$south),
      west = str_rev(tiles$east),
      is_inverted = TRUE
    )
  )
  
  result %>%
    mutate(orientation = list(1:4)) %>%
    unnest_longer(orientation) %>%
    mutate(
      n = case_when(
        orientation == 1 ~ north,
        orientation == 2 ~ east,
        orientation == 3 ~ south,
        orientation == 4 ~ west
      ),
      w = case_when(
        orientation == 1 ~ west,
        orientation == 2 ~ north,
        orientation == 3 ~ east,
        orientation == 4 ~ south
      ),
      s = case_when(
        orientation == 1 ~ south,
        orientation == 2 ~ west,
        orientation == 3 ~ north,
        orientation == 4 ~ east
      ),
      e = case_when(
        orientation == 1 ~ east,
        orientation == 2 ~ south,
        orientation == 3 ~ west,
        orientation == 4 ~ north
      )
    ) %>%
    transmute(
      tile_id_original = tile_id,
      tile_id = str_c(tile_id, "_", orientation, ifelse(is_inverted, "i", "")),
      n,
      e,
      s,
      w
    )
}

