find_max_asteroids <- function(asteroids_str) {
  asteroids_str <- read_lines(asteroids_str)
  asteroids <-
    asteroids_str %>%
    str_c(collapse = "") %>%
    str_split('') %>%
    unlist() %>%
    matrix(ncol = nchar(asteroids_str[[1]]), byrow = TRUE)
  
  coords <-
    which(asteroids == "#", arr.ind = TRUE) %>%
    as_tibble() %>%
    transmute(x = col - 1, y = nrow(asteroids) - row - 1)
  
  result <-
    crossing(a=coords, b=coords) %>%
    as.list() %>%
    bind_cols() %>%
    filter(!(x == x1 & y == y1)) %>% # ?
    mutate(
      slope = (y - y1) / (x - x1),
      quadrant = 1 + (y1 < y) + (x1 < x) + (y1 >= y & x1 < x),
      d = (x - x1)^2 + (y - y1)^2
    ) %>%
    group_by(x, y, slope, quadrant) %>%
    arrange(d) %>%
    slice(1) %>%
    ungroup() %>%
    count(x, y) %>%
    arrange(desc(n))
  result
  
  lst(answer = result$n[[1]], result = result, p = ggplot(result, aes(x, y, label = n)) + geom_point() + geom_label())
}

